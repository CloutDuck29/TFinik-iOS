struct TransactionHistoryResponse: Codable {
    let transactions: [Transaction]
}

struct TransactionHistoryView: View {
    @EnvironmentObject var store: TransactionStore

    @State private var selectedCategory: String? = nil
    @State private var selectedYearMonth: String? = nil
    @State private var hasLoaded = false

    var body: some View {
        ZStack {
            BackgroundView()

            VStack(spacing: 20) {
                // –ó–∞–≥–æ–ª–æ–≤–æ–∫
                HStack {
                    Text("üìù")
                        .font(.system(size: 32))
                    Text("–ò—Å—Ç–æ—Ä–∏—è —Ç—Ä–∞—Ç")
                        .font(.title2.bold())
                        .foregroundColor(.white)
                }
                .padding(.top, 125)

                // –ü–æ–¥–∑–∞–≥–æ–ª–æ–≤–æ–∫
                Text("–ó–¥–µ—Å—å –í—ã –º–æ–∂–µ—Ç–µ —É–≤–∏–¥–µ—Ç—å –∏—Å—Ç–æ—Ä–∏—é —Ç—Ä–∞—Ç –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –ø–æ –Ω–∏–º")
                    .font(.subheadline)
                    .foregroundColor(.gray)
                    .multilineTextAlignment(.center)
                    .padding(.horizontal)

                // –§–∏–ª—å—Ç—Ä—ã –∏ —Å–±—Ä–æ—Å
                HStack(spacing: 12) {
                    Menu {
                        Button("–í—Å–µ –º–µ—Å—è—Ü—ã") { selectedYearMonth = nil }
                        ForEach(uniqueYearMonths(), id: \.self) { ym in
                            Button(ym) { selectedYearMonth = ym }
                        }
                    } label: {
                        Label(selectedYearMonth ?? "–ú–µ—Å—è—Ü", image: "")
                            .labelStyle(EmojiLabelStyle(emoji: "üìÖ"))
                    }

                    Menu {
                        Button("–í—Å–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏") { selectedCategory = nil }
                        ForEach(uniqueCategories(), id: \.self) { category in
                            Button(category) { selectedCategory = category }
                        }
                    } label: {
                        Label(selectedCategory ?? "–ö–∞—Ç–µ–≥–æ—Ä–∏—è", image: "")
                            .labelStyle(EmojiLabelStyle(emoji: "üóÇ"))
                    }

                    Button("‚Ü∫") {
                        selectedCategory = nil
                        selectedYearMonth = nil
                    }
                    .font(.headline)
                    .foregroundColor(.white)
                    .padding(.horizontal, 8)
                    .padding(.vertical, 4)
                    .background(Color.white.opacity(0.1))
                    .cornerRadius(8)
                }
                .font(.footnote)
                .foregroundColor(.gray)

                // –ö–æ–Ω—Ç–µ–Ω—Ç
                if store.transactions.isEmpty {
                    Spacer()
                    ProgressView("–ó–∞–≥—Ä—É–∑–∫–∞...")
                        .progressViewStyle(CircularProgressViewStyle(tint: .white))
                    Spacer()
                } else {
                    // –û–±—â–∞—è —Å—É–º–º–∞ –≤–Ω–µ —Å–∫—Ä–æ–ª–ª–∞
                    let total = filteredAndSortedTransactions().reduce(0) { $0 + $1.amount }
                    Text(String(format: "%.0f‚ÇΩ", total))
                        .font(.title)
                        .bold()
                        .foregroundColor(.white)
                        .padding(.horizontal)
                        .padding(.top, 8)

                    // –°–ø–∏—Å–æ–∫ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–π
                    ScrollView {
                        LazyVStack(spacing: 12) {
                            ForEach(filteredAndSortedTransactions()) { tx in
                                HStack {
                                    ZStack {
                                        Circle()
                                            .fill(iconColor(for: tx.category).opacity(0.2))
                                            .frame(width: 40, height: 40)
                                        Image(systemName: iconName(for: tx.category))
                                            .foregroundColor(iconColor(for: tx.category))
                                    }

                                    VStack(alignment: .leading) {
                                        Text(tx.description)
                                            .lineLimit(1)
                                            .truncationMode(.tail)
                                            .fontWeight(.semibold)
                                            .foregroundColor(.white)
                                        Text(tx.category)
                                            .font(.caption)
                                            .foregroundColor(.gray)
                                    }

                                    Spacer()

                                    VStack(alignment: .trailing) {
                                        Text(tx.date)
                                            .font(.caption)
                                            .foregroundColor(.gray)
                                        Text(String(format: "%.0f‚ÇΩ", tx.amount))
                                            .foregroundColor(tx.isIncome ? .green : .white)
                                            .fontWeight(.semibold)
                                    }
                                }
                                .padding()
                                .background(
                                    RoundedRectangle(cornerRadius: 16)
                                        .fill(Color.white.opacity(0.05))
                                        .overlay(
                                            RoundedRectangle(cornerRadius: 16)
                                                .stroke(Color.purple, lineWidth: 1)
                                        )
                                )
                            }
                        }
                        .padding(.horizontal)
                        .padding(.bottom, 40)
                    }
                }

                Spacer()
            }
            .padding(.bottom, 80)
        }
        .ignoresSafeArea()
        .onAppear {
            if !hasLoaded {
                hasLoaded = true
                store.fetchTransactions()
                DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                    if selectedYearMonth == nil {
                        selectedYearMonth = uniqueYearMonths().first
                    }
                }
            }
        }
    }

    // –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –∏ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞
    func filteredAndSortedTransactions() -> [Transaction] {
        return store.transactions
            .filter { tx in
                let matchCategory = selectedCategory == nil || tx.category == selectedCategory
                let matchYearMonth: Bool
                if let ym = selectedYearMonth, let txDate = parseDate(tx.date) {
                    let formatter = DateFormatter()
                    formatter.locale = Locale(identifier: "ru_RU")
                    formatter.dateFormat = "LLLL yyyy"
                    matchYearMonth = formatter.string(from: txDate).capitalized == ym
                } else {
                    matchYearMonth = true
                }
                return matchCategory && matchYearMonth
            }
            .sorted {
                guard let d1 = parseDate($0.date), let d2 = parseDate($1.date) else { return false }
                return d1 > d2
            }
    }

    func parseDate(_ string: String) -> Date? {
        let formatter = DateFormatter()
        formatter.dateFormat = "yyyy-MM-dd"
        return formatter.date(from: string)
    }

    func uniqueCategories() -> [String] {
        Array(Set(store.transactions.map { $0.category })).sorted()
    }

    func uniqueYearMonths() -> [String] {
        let formatter = DateFormatter()
        formatter.locale = Locale(identifier: "ru_RU")
        formatter.dateFormat = "LLLL yyyy"

        let dates = store.transactions.compactMap { parseDate($0.date) }

        let monthYearTuples = dates.map { date in
            let components = Calendar.current.dateComponents([.year, .month], from: date)
            return components
        }

        let sorted = monthYearTuples
            .compactMap { $0 }
            .sorted {
                if $0.year == $1.year {
                    return ($0.month ?? 0) > ($1.month ?? 0)
                }
                return ($0.year ?? 0) > ($1.year ?? 0)
            }

        var seen: Set<String> = []
        let result = sorted.compactMap { comp -> String? in
            guard let date = Calendar.current.date(from: comp) else { return nil }
            let str = formatter.string(from: date).capitalized
            if seen.contains(str) {
                return nil
            } else {
                seen.insert(str)
                return str
            }
        }

        return result
    }

    func iconName(for category: String) -> String {
        switch category {
        case "–ú–∞–≥–∞–∑–∏–Ω—ã": return "cart.fill"
        case "–¢—Ä–∞–Ω—Å–ø–æ—Ä—Ç": return "bus.fill"
        case "–ü–æ–ø–æ–ª–Ω–µ–Ω–∏–µ": return "plus.circle.fill"
        case "–ü–µ—Ä–µ–≤–æ–¥—ã": return "arrow.left.arrow.right"
        case "–†–∞–∑–≤–ª–µ—á–µ–Ω–∏—è": return "gamecontroller.fill"
        case "–î–æ—Ö–æ–¥": return "creditcard.fill"
        default: return "questionmark.circle"
        }
    }

    func iconColor(for category: String) -> Color {
        switch category {
        case "–ú–∞–≥–∞–∑–∏–Ω—ã": return .purple
        case "–¢—Ä–∞–Ω—Å–ø–æ—Ä—Ç": return .red
        case "–ü–æ–ø–æ–ª–Ω–µ–Ω–∏–µ": return .blue
        case "–ü–µ—Ä–µ–≤–æ–¥—ã": return .orange
        case "–†–∞–∑–≤–ª–µ—á–µ–Ω–∏—è": return .pink
        case "–î–æ—Ö–æ–¥": return .green
        default: return .gray
        }
    }
}

struct EmojiLabelStyle: LabelStyle {
    let emoji: String

    func makeBody(configuration: Configuration) -> some View {
        HStack(spacing: 4) {
            Text(emoji)
            configuration.title
        }
    }
}
